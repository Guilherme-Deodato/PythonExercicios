# Faça um programa que leia um número inteiro e mostre na tela o seu sucessor e seu antecessor.

vlr_entrada = int(input('Insira um valor: '))
sucessor = vlr_entrada + 1
antecessor = vlr_entrada - 1

print(f'O valor digitado foi {vlr_entrada}, seu sucessor é igual a {sucessor} e seu antecessor é {antecessor}')
